export function getProjectLanguage(project: any) {
  return project?.output_language === "Bahasa Melayu"
    ? "Bahasa Melayu"
    : "English";
}

export function translateStatus(status?: string, lang: "English" | "Bahasa Melayu" = "English") {
  const t = translations[lang];

  if (status === "draft") return t.draft;
  if (status === "completed") return t.completed;
  return t.notStarted;
}